#include"share.h"
/*********红外接收模块接10号（红外）口*************/

u8 key=0,n;

void OLED_Show(void);

int main(void)
{
	RCC_Configuration();
	delay_init(72);
	
	Remote_Init();   //红外初始化
	OLED_Init();		 //初始化OLED
  
	OLED_Clear();    //清屏
	OLED_Show();     //屏幕显示
  while(1)
	{
		key=Remote_Scan();
		n=key/100%10+0x30;
		OLED_ShowString(50,3,&n);
		n=key/10%10+0x30;
		OLED_ShowString(60,3,&n);
		n=key%10+0x30;
		OLED_ShowString(70,3,&n);
		delay_ms(400);
	}
}

